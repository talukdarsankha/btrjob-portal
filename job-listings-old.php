<script type="text/javascript">
$(document).ready(function() {
    var isLoading = false;

    $(window).scroll(function() {
        if ($(window).scrollTop() + $(window).height() >= $(document).height() - 100 && !isLoading) {
            isLoading = true;
            $('.loading-spinner').show(); // Show loading spinner
            $.ajax({
                url: 'load_more_jobs.php',
                type: 'GET',
                data: { offset: $('.jp_job_post_main_wrapper_cont').length },
                success: function(response) {
                    $('.jp_job_post_main_wrapper_cont:last').after(response);
                    isLoading = false;
                    $('.loading-spinner').hide(); // Hide loading spinner after data is loaded
                },
                error: function() {
                    isLoading = false;
                    $('.loading-spinner').hide(); // Hide loading spinner in case of error
                }
            });
        }
    });
});

</script>

